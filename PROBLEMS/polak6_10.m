%-----------------------------------------
% Function polak6_10
%	n = 1		m = 25
%	f(x*) = 0.1782
%-----------------------------------------
function y = polak6_10(x)
	d = 1.0/24.0;
	y = 0.0;

	f(1) = (2.0*y.^2 - 1.0).*x(1) + y*(1.0-y)*(1.0-x(1));

	for i = 2:25
		y    = y + d;
		f(i) = (2.0*y^2 - 1.0)*x(1) + y*(1.0-y)*(1.0-x(1));
    end
    
    y = max(f);
end