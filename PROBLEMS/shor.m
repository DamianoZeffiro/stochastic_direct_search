%----------------------------
% Function Shor
%----------------------------
function y = shor(x)
    A = [0 0 0 0 0;
         2 1 1 1 3;
         1 2 1 1 2;
         1 4 1 2 2;
         3 2 1 0 1;
         0 2 1 0 1;
         1 1 1 1 1;
         1 0 1 2 1;
         0 0 2 1 0;
         1 1 2 0 0];
    b = [1; 5; 10; 2; 4; 3; 1.7; 2.5; 6; 3.5];
    X = repmat(x',10,1);
    f = sum((X-A).^2,2).*b;
    y = max(f);
end