%----------------------------
% Function steiner 2
%----------------------------
function y = steiner2(x)
    %keyboard
    a = [0.0 2.0;
         2.0 3.0;
         3.0 -1.0;
         4.0 -0.5;
         5.0 2.0;
         6.0 2.0];
    p = [2 1 1 5 1 1];
    ptilde = [1 1 2 3 2];
     
    f = sqrt(x(1)^2 + x(7)^2) + sqrt((5.5 - x(6))^2+(-1-x(12))^2) + ...
        sum(p'.*sqrt((a(:,1)-x(1:6)).^2+(a(:,2)-x(7:12)).^2)) + ...
        sum(ptilde'.*sqrt((x(1:5)-x(2:6)).^2+(x(7:11) - x(8:12)).^2));

    y = sum(abs(f));
end