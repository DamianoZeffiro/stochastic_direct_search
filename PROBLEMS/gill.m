% Function gill
%----------------------------
function y = gill(x)
    %keyboard
    i = [2:30]'; I = repmat(i,1,9);
    j = [2:10];  J = repmat(j ,29,1);
    X = repmat(x(2:10)',29,1);
    A = sum(X.*(J-1).*((I-1)./29).^(J-2),2);

    I = repmat(i,1,10);
    j = [1:10];  J = repmat(j ,29,1);
    X = repmat(x',29,1);
    B = (sum(X.*((I-1)./29).^(J-1),2)).^2;

    f(1) = sum((x-1).^2) + 1.e-3*sum((x.^2 - 0.25).^2);
    f(2) = sum((A - B - 1).^2) + x(1)^2 + (x(2) - x(1)^2 -1)^2;
    f(3) = sum(100*(x(2:10)- x(1:9).^2).^2 + (1-x(2:10)).^2);

    y = max(f);
end