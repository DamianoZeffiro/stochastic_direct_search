%-----------------------------------------
% Function Mifflin 2 var
%	n = 30
%   f(x*) = -1
%-----------------------------------------
function y = mifflin2var1(x)
n= 30;
    y = 0.0;
    for i = 1:n-1
        f = - x(i) + 2.0*(x(i)^2 + x(i+1)^2-1.0); 
        f = f+ 1.75*abs(x(i)^2 + x(i+1)^2 - 1.0); 
        y = y + f ;
    end

end
