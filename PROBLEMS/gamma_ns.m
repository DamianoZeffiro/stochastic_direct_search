         17.50 20.00 22.50 25.00 30.00 35.00 40.00 50.00 60.00 70.00 80.00 ...
         100.0 150.0 200.0 300.0 500.0 100000];
     
    f = (x(1).*(t + x(2) + 1./(x(3).*t + x(4))).^(t+0.5))./(gamma(t+1).*exp(t)) - 1;
    y = max(abs(f));
end